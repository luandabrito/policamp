
public class DocumentacaoCarro {
	
	private String nomeCliente;
	private String registroCarro;
	private Cliente cnhCliente;
	private String formaPagamento;
	
	public String getNomeCliente() {
		return nomeCliente;
	}
	public void setNomeCliente(String nomeCliente) {
		this.nomeCliente = nomeCliente;
	}
	public String getRegistroCarro() {
		return registroCarro;
	}
	public void setRegistroCarro(String registroCarro) {
		this.registroCarro = registroCarro;
	}
	public Cliente getCnhCliente() {
		return cnhCliente;
	}
	public void setCnhCliente(Cliente cnhCliente) {
		this.cnhCliente = cnhCliente;
	}
	public String getFormaPagamento() {
		return formaPagamento;
	}
	public void setFormaPagamento(String formaPagamento) {
		this.formaPagamento = formaPagamento;
	}
}
	
	
	


